package com.night.test;
//12. Write a Java program to check whether a given string ends with the contents of another string.

public class W3SchoolProblems_02 {

	public static void main(String[] args) {

		String s1 = "Python Exercises";
		String s2 = "Python Exercise";

		System.out.println("\"" + s1 + "\" ends with " + "\"se\"? " + s1.endsWith("se"));
		System.out.println("\"" + s2 + "\" ends with " + "\"se\"? " + s2.endsWith("se"));

	}

}
